    boolean passwordCheck()
    { 
      //System.out.println(verifyPassword.getPassword() + " " + passwordText.getText() );
      return (verifyPassword.getText().equals(passwordText.getText()));
    }
    
    boolean isTextFull()
    {   
        return passwordText.getText().equals(null) && nameText.getText().equals(null) && surnameText.getText().equals(null) && yearText.getText().equals(null);
    }
    
    boolean searchResult()
    {
        SearchAccount search_acc = new SearchAccount("DoctorAccountDatabase.txt", nameText.getText() + " " +surnameText.getText(), passwordText.getText());
        return search_acc.searchSameUsername();
    }
    
    
    boolean generalControl() throws ParseException
    { 
      DateControl dc = new DateControl((yearText.getText()),dayBox.getSelectedItem().toString(),monthBox.getSelectedItem().toString());
      if(isTextFull())
      {
         return false; 
      }
      System.out.println(searchResult());
      if(searchResult())
      {
         return false;
      }
      
      //System.out.println(passwordCheck());
      return passwordCheck() && dc.dateControl();  
    }
    
    void execute_register() throws ParseException
    {
        
        if(generalControl())
        {
            register_account();
            
            this.toBack();
            this.setVisible(false);
            MainMenu mm = new MainMenu();
            mm.setVisible(true);
            mm.toFront();
        }
    }
            
    void register_account()
    {   DateControl control = new DateControl((yearText.getText()),dayBox.getSelectedItem().toString(),monthBox.getSelectedItem().toString());
        control.assignMonthIndex();
        String month = control.monthIndex.get(monthBox.getSelectedItem().toString());
        int day = dayBox.getSelectedIndex()+1;
        int year = Integer.parseInt(yearText.getText()); 
        String birthdate = Integer.toString(day) + "/" + month + "/" + Integer.toString(year);
        Doctor_acc register = new Doctor_acc(nameText.getText(),surnameText.getText(),passwordText.getText(), SubjectBox.getSelectedItem().toString(), birthdate);
        
    }

